const CORS_HEADERS = {
  'Content-Type': 'application/json',
  'Access-Control-Allow-Origin': '*',
};

exports.handler = async (event) => {
  if (event.httpMethod === 'OPTIONS') {
    return { statusCode: 200, headers: CORS_HEADERS, body: '' };
  }

  if (event.httpMethod !== 'POST') {
    return { statusCode: 405, headers: CORS_HEADERS, body: JSON.stringify({ error: 'Nur POST erlaubt.' }) };
  }

  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) {
    return {
      statusCode: 500,
      headers: CORS_HEADERS,
      body: JSON.stringify({ error: 'ANTHROPIC_API_KEY nicht konfiguriert.' }),
    };
  }

  let anfrage;
  try {
    const body = JSON.parse(event.body);
    anfrage = body.anfrage && body.anfrage.trim();
  } catch {
    return { statusCode: 400, headers: CORS_HEADERS, body: JSON.stringify({ error: 'Ungültige Anfrage.' }) };
  }

  if (!anfrage) {
    return { statusCode: 400, headers: CORS_HEADERS, body: JSON.stringify({ error: 'Bitte eine Kundenanfrage eingeben.' }) };
  }

  const heute = new Date().toLocaleDateString('de-DE', { day: '2-digit', month: '2-digit', year: 'numeric' });

  const response = await fetch('https://api.anthropic.com/v1/messages', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'x-api-key': apiKey,
      'anthropic-version': '2023-06-01',
    },
    body: JSON.stringify({
      model: 'claude-sonnet-4-6',
      max_tokens: 4096,
      system: `Du bist Klaus Metzger, Fliesenlegermeister und Inhaber der Fliesen Metzger GmbH.
Du erstellst professionelle Angebote auf Deutsch.
Halte dich exakt an die Preise, das Format und die Struktur aus den Musterangeboten.
Verwende immer diesen Briefkopf:

${BETRIEB_HEADER}

Heutiges Datum: ${heute}`,
      messages: [{
        role: 'user',
        content: `Hier sind 30 Musterangebote unseres Betriebs als Referenz für Preise, Format und Materialien:

${MUSTERANGEBOTE}

---

Erstelle jetzt ein neues, vollständiges Angebot für diese Kundenanfrage.
Nutze exakt das gleiche Format wie die Musterangebote (Positionsliste mit Menge × EP = GP,
Netto / MwSt. 19% / Brutto, Ausführungszeit, Zahlungsbedingungen).
Wähle passende Materialien aus der Musterpreisliste.
Vergib eine neue Angebotsnummer im Format FM-2026-XXX.

KUNDENANFRAGE:
${anfrage}`,
      }],
    }),
  });

  if (!response.ok) {
    const errText = await response.text();
    return {
      statusCode: 500,
      headers: CORS_HEADERS,
      body: JSON.stringify({ error: `Claude API Fehler ${response.status}: ${errText}` }),
    };
  }

  const data = await response.json();
  const angebot = data.content[0].text;

  return {
    statusCode: 200,
    headers: CORS_HEADERS,
    body: JSON.stringify({ angebot }),
  };
};
